// while
// entry control loop check condition before executing statement

let x = 1
while (x < 10) {
   // console.log(x);
    x++
}

// do while
// exit control loop first statement execute then condition check
// it logged value to console once even if the condition fails

let score = 1
do {
    console.log(score);
    score++
}
while(score < 1)